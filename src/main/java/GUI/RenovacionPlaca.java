    private void btnRenovarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRenovarActionPerformed
        if(tablaPlacas.getRowCount() > 0){
            if(tablaPlacas.getSelectedRow() !=-1){
                int id = Integer.parseInt(String.valueOf(tablaPlacas.getValueAt(tablaPlacas.getSelectedRow(), 0)));
                RenovarPlaca pantallaRenovar = new RenovarPlaca(id);
                pantallaRenovar.setVisible(true);
                pantallaRenovar.setLocationRelativeTo(null);

                this.dispose();
            }else{
                mostrarMensaje("No seleccion√≥ ninguna Placa","Error","Error al Renovar");
            }
        }else{
            mostrarMensaje("No hay nada para Renovar en la tabla","Error","Error al Renovar");
        }
    }//GEN-LAST:event_btnRenovarActionPerformed

